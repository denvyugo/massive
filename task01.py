# В диапазоне натуральных чисел от 2 до 99 определить, сколько из них кратны любому из
# чисел в диапазоне от 2 до 9.
n0 = 2
n1 = 99
d0 = 2
d1 = 9
s = 0
for i in range(n0, n1 + 1):
    for j in range(d0, d1 + 1):
        if i % j == 0:
            s += 1
            break

print(f'в диапазоне от {n0} до {n1} {s} чисел кратны любому (хотябы одному) числу в диапазоне от {d0} до {d1}')